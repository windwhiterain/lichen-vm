impl Plugin {
    pub fn generate(&'static self) -> std::io::Result<()> {
        let project = Project::new(self);

        let current_dir = current_dir()?;
        CTX.set(Some(Context {
            current: GenerateStage::Lib,
            plugin: self,
        }));
        let lib_path = current_dir
            .join(self.lib_crate_path)
            .join("src")
            .join(self.lib_module.relative)
            .with_extension("rs");
        println!("generate lib at: {}", lib_path.to_string_lossy());
        let mut file = OpenOptions::new()
            .write(true)
            .truncate(true)
            .create(true)
            .open(&lib_path)?;

        writeln!(
            file,
            "// This file is @generated by the {} plugin codegen. Do not edit by hand.",
            self.name
        )?;
        writeln!(file, "{}", DisplayProjectLib(&project))?;
        drop(file);
        CTX.set(Some(Context {
            current: GenerateStage::Bin,
            plugin: self,
        }));
        let bin_path = match &self.bin_module {
            Module::Symbol { crate_path, symbol } => current_dir
                .join(crate_path)
                .join("src")
                .join(symbol.relative),
            Module::Path(path) => current_dir.join(path),
        }
        .with_extension("rs");
        println!("generate bin at: {}", bin_path.to_string_lossy());
        let mut file = OpenOptions::new()
            .write(true)
            .truncate(true)
            .create(true)
            .open(&bin_path)?;
        writeln!(
            file,
            "// This file is @generated by the {} plugin codegen. Do not edit by hand.",
            self.name
        )?;
        writeln!(file, "{}", DisplayProjectBin(&project))?;
        drop(file);

        for path in [&lib_path, &bin_path] {
            if let Err(e) = std::process::Command::new("rustfmt")
                .arg(path)
                .current_dir(&current_dir)
                .status()
            {
                eprintln!(
                    "warning: failed to run rustfmt on {}: {}",
                    path.display(),
                    e
                );
            }
        }

        CTX.set(None);
        Ok(())
    }
}

