#[derive(Clone, Copy)]
pub enum Symbol {
    Written(&'static WrittenSymbol),
    GeneratedLib {
        plugin: &'static Plugin,
        relative: &'static str,
        name: &'static (dyn Display + Sync),
    },
    Raw(&'static str),
    Dyn(&'static (dyn Display + Sync)),
    ThisGeneratedLib {
        relative: &'static str,
        name: &'static str,
    },
    GeneratedBin {
        relative: &'static str,
        name: &'static str,
    },
    Self_,
}

impl Display for Symbol {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Symbol::Written(symbol) => write!(f, "{}", symbol),
            Symbol::GeneratedLib {
                plugin,
                relative,
                name,
            } => {
                write!(f, "{}", plugin.lib_module)?;
                if !relative.is_empty() {
                    write!(f, "::{relative}")?;
                }
                write!(f, "::{name}")?;
                Ok(())
            }
            Symbol::Raw(symbol) => write!(f, "{}", symbol),
            Symbol::Dyn(symbol) => write!(f, "{}", symbol),
            Symbol::ThisGeneratedLib { relative, name } => {
                write!(f, "{ThisGeneratedLib}")?;
                if !relative.is_empty() {
                    write!(f, "::{relative}")?;
                }
                writeln!(f, "::{}", name)?;
                Ok(())
            }
            Symbol::GeneratedBin { relative, name } => {
                write!(f, "crate")?;
                if !relative.is_empty() {
                    write!(f, "::{relative}")?;
                }
                writeln!(f, "::{}", name)?;
                Ok(())
            }
            Symbol::Self_ => write!(f, "Self"),
        }
    }
}

